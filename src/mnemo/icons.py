def icon_label(name, size=20, color=None):
    from PyQt6.QtWidgets import QLabel
    lbl = QLabel()
    pm = icon_pixmap(name, size, color)
    if not pm.isNull():
        lbl.setPixmap(pm)
    lbl.setFixedSize(size, size)
    return lbl
